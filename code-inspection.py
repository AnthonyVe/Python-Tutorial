# Define the Vehicle class.
class Vehicle:
	name = ""
	kind = "car"
	color = ""
	value = 100.00
	def description(self):
		desc_str = "%s is a %s %s worth $%.2f." % (self.name, self.color, self.kind, self.value)
		return desc_str
		
car1 = Vehicle()
car1.name = "darren"
car1.color = "brown"
car1.kind = "toyota corolla"
car1.value = 2.00

print(car1.description())